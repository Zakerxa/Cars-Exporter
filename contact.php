        window.addEventListener('DOMContentLoaded', loading('block'), false);

        $(document).ready(function() {

            setTimeout(() => loading('none'), 100);

            new Vue({
                el: '#contact',
                data() {
                    return {
                        navIcon: 'navbar-toggler-icon',
                        navTran: 'transform: rotateZ(0deg);',
                        navCheck: false,
                        phone: '',
                        email: '',
                        name: '',
                        message: '',
                        error: '',
                        loading: false,
                        success: false
                    }
                },
                methods: {
                    navigatorClick() {
                        this.navTran = "transition:transform 0.5s;transform: rotateZ(90deg); "
                        this.navCheck = !this.navCheck;
                        if (this.navCheck) this.navIcon = "btn-close btn-close-white ";
                        else {
                            this.navIcon = "navbar-toggler-icon ";
                            this.navTran = "transition:transform 0.5s; "
                        }
                    },
                    send() {
                        // const redirect = `mailto:info@zakerxa.com?mailto:?Content-type=text/html&subject=THIDAAUTOCENTER.COM&body=Name : ${this.name},%0D%0A %0D%0A ${this.message}`;

                        if (this.name == '' || this.email == '' || this.phone == '') {

                        } else {
                            this.loading = true;
                            $.ajax({
                                url: 'send.php',
                                method: "post",
                                data: {
                                    name: this.name,
                                    email: this.email,
                                    phone: this.phone,
                                    message: this.message
                                },
                                success: (res) => {
                                    console.log(res)
                                    this.loading = false;
                                    if (res == 'sent') {
                                        this.success = true;
                                        this.error = '';
                                    } else {
                                        this.error = res;
                                        this.success = false;
                                    }
                                },
                                error: e => {
                                    this.loading = false;
                                    console.log(e);
                                }
                            })
                        }
                    }
                }
            })

        });

        // Get Loading By Id
        function loading(e) {
            document.getElementById('loading').style.display = e;
        }
