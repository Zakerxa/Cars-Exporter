                                slidesToScroll: 3,
                            }
                        }, {
                            breakpoint: 480,
                            settings: {
                                arrows: false,
                                slidesToShow: 3,
                                slidesToScroll: 3,
                                mobileFirst: true,
                            }
                        }]
                    });

                    $('.slider-for').not('.slick-initialized').slick({
                        slidesToShow: 1,
                        slidesToScroll: 1,
                        arrows: false,
                        fade: true,
                        asNavFor: '.slider-nav'
                    });
