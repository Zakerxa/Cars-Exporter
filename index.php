                            this.navIcon = "navbar-toggler-icon ";
                            this.navTran = "transition:transform 0.5s; "
                        }
                    },
                },
                created() {},
                mounted() {
                    $(window).resize(function() {
                        $('.cars')[0].slick.refresh();
                    });

                    $('.cars').slick({
                        centerMode: false,
                        slidesToShow: 8,
                        infinite: true,
                        slidesToScroll: 3,
                        responsive: [{
                            breakpoint: 1000,
                            settings: {
                                slidesToShow: 5,
                                slidesToScroll: 3,
                                arrows: false,
                            }
                        }, {
                            breakpoint: 768,
                            settings: {
                                arrows: false,
                                centerPadding: '40px',
